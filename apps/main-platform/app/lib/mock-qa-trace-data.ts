function ensureMinLength(content: string, appendBlocks: string[], minLength = 2400): string {
  let next = content.trim();
  let cursor = 0;
  const normalizedBlocks = appendBlocks.map((block) => block.trim()).filter(Boolean);
  while (next.length < minLength) {
    const block = normalizedBlocks[cursor % normalizedBlocks.length] ?? "";
    if (!block) {
      break;
    }
    next += `\n\n${block}`;
    cursor += 1;
  }
  return next;
}

