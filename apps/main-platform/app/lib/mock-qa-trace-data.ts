export const TRACE_REFERENCE_DOCS: TraceReferenceDoc[] = TRACE_REFERENCE_META.map((meta) => {
  const normalizedExcerpt = normalizeExcerpt(meta);
  return {
    id: `${meta.traceCaseId}-ref-${meta.refIndex}`,
    ...meta,
    excerpt: normalizedExcerpt,
    fullText: buildReferenceArticle(meta, normalizedExcerpt),
  };
});

export function getTraceReferencesByCase(traceCaseId: TraceCaseId): TraceReferenceDoc[] {
  return TRACE_REFERENCE_DOCS
    .filter((doc) => doc.traceCaseId === traceCaseId)
    .sort((a, b) => a.refIndex - b.refIndex);
}

export function findMockQAPair(question: string): MockQAPair | null {
  const normalized = question.replace(/\s+/g, "").replace(/[“”"'‘’]/g, "");
  const hit = MOCK_QA_PAIRS.find((pair) =>
    pair.matchers.every((matcher) => normalized.includes(matcher.replace(/\s+/g, "").replace(/[“”"'‘’]/g, ""))),
  );
  return hit ?? null;
}
