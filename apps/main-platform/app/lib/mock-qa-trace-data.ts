function buildEvidenceParagraphs(meta: TraceReferenceMeta, indent: string): string {
  const highlightFallback = meta.highlightTerms[0] ?? "核心条文";
  return meta.evidencePoints
    .map((point, index) => {
      const highlight = meta.highlightTerms[index % meta.highlightTerms.length] ?? highlightFallback;
      return `${indent}（${index + 1}）围绕“${point}”的审查应与“${highlight}”形成一一对应：先确认该事实是否真实存在，再判断其是否触发规范要件，最后说明其对责任范围、给付顺序或抗辩成立的具体影响。`;
    })
    .join("\n\n");
}

function buildSupplementBlocks(meta: TraceReferenceMeta, indent: string): string[] {
  const firstEvidence = meta.evidencePoints[0] ?? "关键事实";
  const secondEvidence = meta.evidencePoints[1] ?? "争议事实";
  const firstHighlight = meta.highlightTerms[0] ?? "核心规则";
  const secondHighlight = meta.highlightTerms[1] ?? firstHighlight;
  return [
    `${indent}延伸评注一：在司法论证中，引用规范并不等于完成说理。若未把“${firstEvidence}”与“${firstHighlight}”建立因果桥接，结论仍可能停留在抽象判断层面。为避免这一问题，建议在裁判文书或研究文本中同步呈现“事实来源—要件匹配—结论导出”的完整链条。`,
    `${indent}延伸评注二：当同一争议同时触及实体规则与程序规则时，应先回答程序状态对实体判断是否构成“必要前提”，再决定是否中止、驳回或继续审理。该步骤有助于防止将阶段性程序结果误读为终局法律效果，并提升结论的可复核性。`,
    `${indent}延伸评注三：对“${secondEvidence}”的处理通常决定案件边界。若该事实证明不足，应明确不利后果归属；若证据充足，则应进一步解释其如何影响“${secondHighlight}”的适用强度。通过边界说明，文章不仅回答“是什么”，也能回答“为什么不是其他结论”。`,
  ];
}

function normalizeExcerpt(meta: TraceReferenceMeta, targetLength = 150, maxLength = 180): string {
  let excerpt = meta.excerpt.replace(/\s+/g, "").trim();
  if (excerpt.length < targetLength) {
    const firstEvidence = meta.evidencePoints[0] ?? "关键争点";
    const firstHighlight = meta.highlightTerms[0] ?? "核心规则";
    const bridge = `该规则在${meta.applyScene}中可直接用于说明${firstEvidence}与${firstHighlight}之间的对应关系，并据此形成可复核的法律结论。`;
    if (!excerpt.endsWith("。")) {
      excerpt += "。";
    }
    excerpt += bridge;
  }

  if (excerpt.length > maxLength) {
    const clipped = excerpt.slice(0, maxLength);
    const punctuation = Math.max(clipped.lastIndexOf("。"), clipped.lastIndexOf("；"), clipped.lastIndexOf("，"));
    if (punctuation >= Math.floor(targetLength * 0.9)) {
      return clipped.slice(0, punctuation + 1);
    }
    return clipped;
  }

  return excerpt;
}

